[<AutoOpen>]
module Actions

let add (leftSide: float) (rightSide: float) =
  leftSide + rightSide
  
let substract (leftSide: float) (rightSide: float) =
  leftSide - rightSide

let multiply (leftSide: float) (rightSide: float) =
  leftSide * rightSide
  
let divide (leftSide: float) (rightSide: float) =
  leftSide / rightSide